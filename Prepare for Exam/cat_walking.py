minute_walk_per_day = int(input())
walks_num_per_day = int(input())
cat_calories_per_day = int(input())


total_min_walks = walks_num_per_day * minute_walk_per_day
total_burnt_calories = total_min_walks * 5

if total_burnt_calories >= cat_calories_per_day / 2:
    print(f"Yes, the walk for your cat is enough. Burned calories per day: {total_burnt_calories}.")
else:
    print(f"No, the walk for your cat is not enough. Burned calories per day: {total_burnt_calories}.")
